def write_markdown(path: Path, cases: list[Case]) -> None:
    lines = [
        "# 联盟对决测试用例",
        "",
        "## 需求确认项",
        "",
        "### 已确认",
    ]
    lines.extend(f"- {item}" for item in CONFIRMED)
    lines.extend(["", "### 待确认"])
    lines.extend(f"- {item}" for item in PENDING)
    lines.extend(["", "### 继承逻辑"])
    lines.extend(f"- {item}" for item in INHERITED)
    lines.extend(["", "### 基于经验补充的风险"])
    lines.extend(f"- {item}" for item in RISKS)
    lines.extend(["", "## 测试用例导入表", ""])
    lines.append("| " + " | ".join(HEADERS) + " |")
    lines.append("|" + "|".join(["---"] * len(HEADERS)) + "|")
    for case in cases:
        row = [case.title, case.group, case.owner, case.precondition, numbered(case.steps), numbered(case.expected), case.level, case.case_type]
        lines.append("| " + " | ".join(md_cell(v) for v in row) + " |")
    lines.extend(["", "## 需要补充的信息"])
    lines.extend(f"- {item}" for item in PENDING)
    path.write_text("\n".join(lines), encoding="utf-8")


def write_workbook(path: Path, cases: list[Case]) -> None:
    wb = Workbook()
    ws = wb.active
    ws.title = "导入用例"
    ws.append(HEADERS)
    bold = Font(bold=True)
    wrap = Alignment(vertical="top", wrap_text=True)
    for cell in ws[1]:
        cell.font = bold
        cell.alignment = wrap
    for case in cases:
        ws.append([case.title, case.group, case.owner, case.precondition, numbered(case.steps), numbered(case.expected), case.level, case.case_type])
    for col, width in {"A": 34, "B": 34, "C": 10, "D": 32, "E": 54, "F": 54, "G": 8, "H": 10}.items():
        ws.column_dimensions[col].width = width
    for row in ws.iter_rows():
        for cell in row:
            cell.alignment = wrap
    ws.freeze_panes = "A2"

    ws2 = wb.create_sheet("需求确认")
    ws2["A1"] = "分类"
    ws2["B1"] = "内容"
    ws2["A1"].font = bold
    ws2["B1"].font = bold
    ws2.column_dimensions["A"].width = 16
    ws2.column_dimensions["B"].width = 120
    row = 2
    for label, items in [("已确认", CONFIRMED), ("待确认", PENDING), ("继承逻辑", INHERITED), ("经验风险", RISKS)]:
        for item in items:
            ws2.cell(row=row, column=1, value=label)
            ws2.cell(row=row, column=2, value=item)
            row += 1
    for r in ws2.iter_rows():
        for cell in r:
            cell.alignment = wrap
    wb.save(path)


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--outdir", type=Path, default=Path("generated") / "alliance_duel")
    args = parser.parse_args()
    outdir = args.outdir
    outdir.mkdir(parents=True, exist_ok=True)
    cases = build_cases()
    write_markdown(outdir / "同盟对决_测试用例.md", cases)
    write_workbook(outdir / "同盟对决_测试用例.xlsx", cases)
    print(f"Generated {len(cases)} cases in {outdir.resolve()}")


if __name__ == "__main__":
    main()
